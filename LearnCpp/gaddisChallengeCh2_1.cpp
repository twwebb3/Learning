/*  Sum of Two Numbers
Write a program that stores the integers 62 and 99 in variables,
and stores the sum of these two in a variable names total.
*/

#include <iostream>
using namespace std;

int main()
{
	int x=62, y=99, total;
	total=x+y;
	cout << "The sum is: "
		 << total
		 << endl;

	return 0;
}
